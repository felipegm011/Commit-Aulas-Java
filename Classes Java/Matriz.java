
public class Matriz {

	public static void main(String[] args) {
		
		int jogo/*COLUNA*/ [] /* LINHA*/ [] = new int [3] [32];
		
		// exemplo de uma tabela com 3 colunas e 3 linhas
		
		//COLUNA
		jogo[0] [0] = 1;	// coluna 1 na linha 1 
		jogo[0] [1] = 1;	// coluna 1 na linha 2
		jogo[0] [2] = 1;	// coluna 1 na linha 3 
		// LINHA
		jogo[1] [0] = 1;	// coluna 2 na linha 1 
		jogo[1] [1] = 1;	// coluna 2 na linha 2
		jogo[1] [2] = 1;	// coluna 2 na linha 3
		

	}

}
